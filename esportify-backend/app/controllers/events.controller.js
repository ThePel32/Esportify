exports.create = (req, res) => {
    if (!req.body) {
        return res.status(400).send({ message: "Le contenu ne peut pas Ãªtre vide !" });
    }

    const token = req.headers['authorization'] ? req.headers['authorization'].split(' ')[1] : null;
    if (!token) {
        return res.status(401).send({ message: "Token manquant, veuillez vous authentifier." });
    }

    jwt.verify(token, process.env.JWT_SECRET, (err, decoded) => {
        if (err) {
            return res.status(401).send({ message: "Token invalide ou expirÃ©." });
        }

        const event = new Event({
            title: req.body.title,
            description: req.body.description,
            date_time: req.body.date_time,
            max_players: req.body.max_players,
            organizer_id: decoded.id,
            state: "pending",
            images: req.body.images,
            duration: req.body.duration || 1,
            created_at: new Date(),
            updated_at: new Date(),
        });

        Event.create(event, (err, data) => {
            if (err) {
                return res.status(500).send({ message: err.message || "Erreur lors de la crÃ©ation de l'Ã©vÃ©nement." });
            }
            res.send(data);
        });
    });
};

